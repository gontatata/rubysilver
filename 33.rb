# Threadクラスのオブジェクトを生成するために使用できるメソッドには、以下のようなものがあります。

# Thread.new：新しいスレッドを生成するために使用される一般的なメソッドです。引数には、実行したいブロックやメソッドを渡すことができます。
# Thread.fork：Thread.newと同様に、新しいスレッドを生成するために使用されます。引数には、実行したいブロックやメソッドを渡すことができます。
# Thread.start：Thread.newと同様に、新しいスレッドを生成するために使用されます。引数には、実行したいブロックやメソッドを渡すことができます。
# Thread.spawn：Thread.newと同様に、新しいスレッドを生成するために使用されます。引数には、実行したいブロックやメソッドを渡すことができます。
# これらのメソッドを使用することで、Threadクラスのオブジェクトを生成し、複数のスレッドを実行することができます。