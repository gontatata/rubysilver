a = [1, 2, 5, 7, 8]
b = [1, 3, 6, 7, 9]
c =  nil || a & b | a && a | b
p c
# ||は左辺から順番に式を評価して「真」の時点でその値を返す
# &&のほうが優先度が高い
# c =  nil || (a & b) | (a && a) | b のように計算の優先づけがされる
# a & b は[1, 7],a&&a =>[1,2,5,7,8]



# |は和集合
a = [1, 2, 3]
b = [3, 4, 5]
c = a | b
puts c.inspect  # => [1, 2, 3, 4, 5]

a = nil
b = 1

c = a | b
puts c  # => 1

d = a || b
puts d  # => 1